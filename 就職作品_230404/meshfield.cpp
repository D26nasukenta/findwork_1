void MeshField::Init()
{
	//頂点バッファ生成
	{
		for (int x = 0; x <= Field_X; x++)
		{
			for (int z = 0; z <= Field_Z; z++)
			{
				float y = g_FieldHeight[x][z];
				m_Vertex[x][z].Position =
					D3DXVECTOR3((x - 10) * 5.0f, y, (z - 10) * -5.0f);
				m_Vertex[x][z].Normal =
					D3DXVECTOR3(0.0f, 1.0f, 0.0f);
				m_Vertex[x][z].Diffuse =
					D3DXVECTOR4(1.0f, 1.0f, 1.0f, 1.0f);
				m_Vertex[x][z].TexCoord =
					D3DXVECTOR2(x * 0.5f, z * 0.5f);
			}

		}
		//頂点バッファ生成
		D3D11_BUFFER_DESC bd;//<-これもゼロクリアする。(構造体内をすべてゼロに)
		//構造体内をすべてゼロにする。
		ZeroMemory(&bd, sizeof(bd));

		bd.Usage = D3D11_USAGE_DEFAULT;
		bd.ByteWidth = sizeof(VERTEX_3D) * 21 * 21; //頂点数分かける　x * z
		bd.BindFlags = D3D11_BIND_VERTEX_BUFFER;
		bd.CPUAccessFlags = 0;

		D3D11_SUBRESOURCE_DATA sd;
		ZeroMemory(&sd, sizeof(sd));
		sd.pSysMem = m_Vertex;

		Renderer::GetDevice()->CreateBuffer(&bd, &sd, &m_VertexBuffer);

	}

	//法線ベクトル算出

	for (int x = 1; x <= 19; x++)
	{
		for (int z = 1; z <= 19; z++)
		{
			D3DXVECTOR3 vx, vz, vn;

			vx = m_Vertex[x + 1][z].Position
				- m_Vertex[x - 1][z].Position;

			vz = m_Vertex[x][z - 1].Position
				- m_Vertex[x][z + 1].Position;

			D3DXVec3Cross(&vn, &vz, &vx);//外積

			D3DXVec3Normalize(&vn, &vn);//正規化(長さを1に)

			m_Vertex[x][z].Normal = vn;

		}
	}

	//インデックスバッファ生成
	{
		unsigned int index[(22 * 2) * 20 - 2];

		int i = 0;
		for (int x = 0; x < Field_X; x++)
		{
			for (int z = 0; z < Field_Z + 1; z++)
			{
				index[i] = x * 21 + z;
				i++;

				index[i] = (x + 1) * 21 + z;
				i++;
			}
			//縮退ポリゴン
			if (x == 19)//一番最後の縮退いらないからブレイクする
			{
				break;
			}
			//終わりの頂点
			index[i] = (x + 1) * 21 + 20;
			i++;
			//始めの頂点
			index[i] = (x + 1) * 21;
			i++;
		}
		//頂点バッファ生成
		D3D11_BUFFER_DESC bd;//<-これもゼロクリアする。(構造体内をすべてゼロに)
		//構造体内をすべてゼロにする。
		ZeroMemory(&bd, sizeof(bd));

		bd.Usage = D3D11_USAGE_DEFAULT;
		bd.ByteWidth = sizeof(unsigned int) * ((22 * 2) * 20 - 2);
		bd.BindFlags = D3D11_BIND_INDEX_BUFFER;
		bd.CPUAccessFlags = 0;

		D3D11_SUBRESOURCE_DATA sd;
		ZeroMemory(&sd, sizeof(sd));
		sd.pSysMem = index;

		Renderer::GetDevice()->CreateBuffer(&bd, &sd, &m_IndexBuffer);

	}

	//テクスチャーの読み込み
	D3DX11CreateShaderResourceViewFromFile
	(Renderer::GetDevice(),
		"asset/texture/grass_001.jpg",
		NULL,
		NULL,
		&m_Texture,
		NULL);

	assert(m_Texture);

	Renderer::CreateVertexShader(&m_VertexShader,
		&m_VertexLayout,
		"unlitTextureVS.cso");

	Renderer::CreatePixelShader(&m_PixelShader, "unlitTexturePS.cso");

	Scene* scene = Manager::GetScene();
	m_SE = scene->AddGameObject<Audio>(2);
	m_SE->Load("asset\\audio\\BGM_001.wav");

	m_SE->Play(true ,0.1f);

	m_Position = D3DXVECTOR3(0.0f, 0.0f, 0.0f);
	m_Rotation = D3DXVECTOR3(0.0f, 0.0f, 0.0f);
	m_Scale = D3DXVECTOR3(1.0f, 1.0f, 1.0f);

}

void MeshField::Uninit()
{
	m_VertexBuffer->Release();
	m_IndexBuffer->Release();
	m_Texture->Release();

	m_VertexLayout->Release();
	m_VertexShader->Release();
	m_PixelShader->Release();

}

